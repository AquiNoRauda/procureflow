export async function exportOrderPDF(items: PurchaseItem[], customer?: string): Promise<void> {
  if (items.length === 0) return;

  // Group by supplier
  const grouped: Record<string, PurchaseItem[]> = {};
  for (const item of items) {
    if (!grouped[item.supplier]) grouped[item.supplier] = [];
    grouped[item.supplier].push(item);
  }

  const groups: SupplierGroup[] = Object.entries(grouped)
    .map(([supplier, supplierItems]) => ({
      supplier,
      items: supplierItems,
      totalQty: supplierItems.reduce((s, i) => s + i.quantity, 0),
    }))
    .sort((a, b) => a.supplier.localeCompare(b.supplier));

  const orderDate = new Date().toLocaleDateString('en-US', {
    weekday: 'long',
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  });

  const html = buildHtml(groups, orderDate, customer);

  const { uri } = await Print.printToFileAsync({ html, base64: false });

  const canShare = await Sharing.isAvailableAsync();
  if (canShare) {
    await Sharing.shareAsync(uri, {
      mimeType: 'application/pdf',
      UTI: 'com.adobe.pdf',
      dialogTitle: 'Export Purchase Order',
    });
  }
}

