interface SupplierGroup {
  supplier: string;
  items: PurchaseItem[];
  totalQty: number;
}

// Accent color per supplier (fallback to a neutral blue)
const SUPPLIER_ACCENT: Record<string, string> = {
  'Fresh Farms': '#2E7D32',
  'Prime Meats Co': '#AD1457',
  'Ocean Catch': '#1565C0',
  'Valley Dairy': '#E65100',
  'Metro Supply': '#6A1B9A',
  'Drinks Direct': '#00838F',
  CleanPro: '#37474F',
};

